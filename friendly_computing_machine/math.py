#!/usr/bin/env python3
"""A small set of functions for doing math operations.
"""

# Write a function named add that adds two values
def add(a, b):
    """Function that adds two arguments
    """
    return a + b

def sub(a, b):
    """Function that subtracts two arguments
    """
    return a - b

def mult(a, b):
    """Function that multiplies two arguments
    """
    return a * b

def div(a, b):
    """Function that divides two arguments
    """
    return a / b

def intDiv(a, b):
    """Function that divides two arguments with integer division
    """
    return a // b
